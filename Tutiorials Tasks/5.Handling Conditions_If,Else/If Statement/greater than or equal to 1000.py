"""
Find out whether the entered number is greater than or equal to 1000 or not
"""

num = int(input("Enter The Number:"))

if num < 1000:
    print("The Number You Entered Is",(num),"Which Is Smaller Than 1000 . And not equal to 1000")
    
if num == 1000:
    print("The Number You Entered Is",(num),"Which Is Equal To 1000")    
    
if num > 1000:
    print("The Number You Entered Is",(num),"Which Is Greater Than 1000 . And not equal to 1000")    
    
    