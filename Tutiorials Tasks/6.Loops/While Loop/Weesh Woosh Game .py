"""Program For Weesh Woosh Game""" 


num = 0


while num<=100:
    
    if num%3 == 0 and num%5==0:
        print("Weesh Woosh")
        
        
        
        
        
    if num%3 == 0:
        print("Weesh ")
        
        
        
        
        
    if num%5 == 0:
        print("Woosh")
        
        
        
        
        
    else:
        print(num)
      
    num = num + 1   
        


