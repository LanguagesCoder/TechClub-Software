pi=22/7
radius = float(input("Enter the Radius of the Cone: "))
slant_height = float(input("Enter the slant_height of the Cone: "))
Height = float(input("Enter the Height of the Cone: "))
volume = 1/3(pi * radius * radius * Height)
area = pi * radius * slant_height + pi * radius^2
perimeter = (1/3) * pi * radius^2 * Height
print("The Area of the Cone is "+str(area))
print("The Area of the Cone is "+str(volume))
print("The Area of the Cone is "+str(perimeter))